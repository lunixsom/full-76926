/*
    EJEMPLOS DE OPERADORES ARITMÉTICOS
*/

let a = 10;
let b = 5;
let result;

// Suma
resultado = a + b;
console.log("Suma", resultado);     // Devuelve: 15

// Resta
resultado = a - b;
console.log("Resta", resultado);    // Devuelve: 5

// Producto (Multiplicación)
resultado = a * b;
console.log("Producto", resultado); // Devuelve: 50

// División
resultado = a / b;
console.log("División", resultado); // Devuelve: 2

// Módulo o Resto
resultado = a % b;
console.log("Resto", resultado);    // Devuelve: 0